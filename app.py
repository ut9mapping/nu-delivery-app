    if lat:
        st.success(f"üìç ‡∏û‡∏¥‡∏Å‡∏±‡∏î GPS ‡∏û‡∏£‡πâ‡∏≠‡∏°: {lat}, {lon}")
    else:
        st.warning("üì° ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏£‡∏≠‡∏û‡∏¥‡∏Å‡∏±‡∏î GPS... (‡πÇ‡∏õ‡∏£‡∏î‡∏Å‡∏î‡∏¢‡∏≠‡∏°‡∏£‡∏±‡∏ö‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡πå‡πÉ‡∏ô‡πÄ‡∏ö‡∏£‡∏≤‡∏ß‡πå‡πÄ‡∏ã‡∏≠‡∏£‡πå)")

    st.subheader("üè† ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏™‡∏ñ‡∏≤‡∏ô‡∏ó‡∏µ‡πà")
    place_name = st.text_input("‡∏ä‡∏∑‡πà‡∏≠‡∏™‡∏ñ‡∏≤‡∏ô‡∏ó‡∏µ‡πà / ‡∏ö‡πâ‡∏≤‡∏ô‡πÄ‡∏•‡∏Ç‡∏ó‡∏µ‡πà (‡∏à‡∏≥‡πÄ‡∏õ‡πá‡∏ô)")

    with st.expander("üìç ‡∏£‡∏∞‡∏ö‡∏∏‡∏£‡∏≤‡∏¢‡∏•‡∏∞‡πÄ‡∏≠‡∏µ‡∏¢‡∏î‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á (‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà‡∏Å‡πá‡πÑ‡∏î‡πâ)", expanded=False):
        c1, c2 = st.columns(2)
        g = c1.selectbox("1. ‡∏õ‡∏£‡∏∞‡∏ï‡∏π", get_opts("‡∏õ‡∏£‡∏∞‡∏ï‡∏π"))
        z = c2.selectbox("2. ‡∏ù‡∏±‡πà‡∏á‡∏ñ‡∏ô‡∏ô/‡πÇ‡∏ã‡∏ô", get_opts("‡∏ù‡∏±‡πà‡∏á‡∏ñ‡∏ô‡∏ô/‡πÇ‡∏ã‡∏ô", {"‡∏õ‡∏£‡∏∞‡∏ï‡∏π": g}))

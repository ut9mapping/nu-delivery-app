    with st.expander("üìä ‡∏î‡∏π‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î"):
        sheet = get_sheet()
        if sheet:
            st.dataframe(pd.DataFrame(sheet.get_all_records()))
